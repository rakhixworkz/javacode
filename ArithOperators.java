class ArithOperators
{
	public static void main(String args[])
	{
		int a=50;
		int b=10;
		int c=a+b;
		int d=a-b;
		int e=a*b;
		int f=a/b;
		int g=a%b;
		int h=--a;
		int k=b++;
		System.out.println("value of addition is=" +c);
		System.out.println("value of subtraction is=" +d);
		System.out.println("value of multication is=" +e);
		System.out.println("value of division is=" +f);
		System.out.println("value of modulus is=" +g);
		System.out.println("value of predecrement is=" +h);
		System.out.println("value of postincreament is=" +k);
	}
}
		
		
		